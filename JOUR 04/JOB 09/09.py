# Liste donnée
L = [8, 24, 27, 48, 2, 16, 9, 102, 7, 84, 91]

# Récupérer la valeur, le maximum et le minimum des éléments de la liste
valeur = L[0]
maximum = max(L)
minimum = min(L)

# Afficher les résultats
print(f"La valeur de la première élément dans la liste est : {valeur}")
print(f"Le maximum dans la liste est : {maximum}")
print(f"Le minimum dans la liste est : {minimum}")
